#include "ft_rush02.h"

t_llui	ft_atoi(char *str)
{
	int		cmpt;
	t_llui	nb;

	nb = 0;
	cmpt = 0;
	while (str[cmpt] >= '0' && str[cmpt] <= '9')
	{
		nb *= 10;
		nb += str[cmpt] - 48;
		cmpt++;
	}
	return (nb);
}
