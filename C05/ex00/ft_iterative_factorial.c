int	ft_iterative_factorial(int nb)
{
	int	i;

	if (nb == 0)
		return (1);
	i = nb;
	if (nb > 0)
	{
		while (i > 1)
		{
			i--;
			nb = nb * i;
		}
		return (nb);
	}
	return (0);
}
