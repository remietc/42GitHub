#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	i;
	int	j;

	i = 1;
	j = 0;
	(void) argc;
	while (argv[i])
		i++;
	i--;
	while (i > 0)
	{
		while (argv[i][j])
		{
			write(1, &argv[i][j], 1);
			j++;
		}
		write(1, "\n", 1);
		i--;
		j = 0;
	}
	return (0);
}
