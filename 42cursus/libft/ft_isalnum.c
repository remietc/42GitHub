#include "libft.h"

int	ft_isalnum(int c)
{
	int	a;

	a = 0;
	if ((c >= '0' && c <= '9') || (c >= 'A' && c <= 'Z')
		|| (c >= 'a' && c <= 'z'))
	{
		a = 8;
	}
	return (a);
}
