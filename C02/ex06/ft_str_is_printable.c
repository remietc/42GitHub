int	ft_str_is_printable(char *str)
{
	int	i;
	int	a;
	int	b;

	i = 0;
	a = 0;
	b = 0;
	while (str[i])
	{
		if (str[i] < 32 || str[i] > 126)
			a++;
		i++;
	}
	if (i == 0 || a == 0)
		b = 1;
	else
		b = 0;
	return (b);
}
